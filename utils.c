#include "fdf.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	ft_putfinish(char *s)
{
	ft_putstr(s);
	exit(1);
}
